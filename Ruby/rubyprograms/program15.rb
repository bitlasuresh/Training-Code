months = gets.chomp.to_i

case months

when 1
	puts "Jan"

when 2
	puts "Feb"

when 3
	puts "Mar"

when 4
	puts "Apr"

when 5
	puts "May"

when 6
	puts "Jun"

when 7
	puts "Jul"

when 8
	puts "Aug"
	
when 9
	puts "Sep"	
	
when 10
	puts "Oct"

when 11
	puts "Nov"
	
when 12
	puts "Dec"
	
else
	puts "Please enter correct number "
	
end